package cursojava.aulas.aula5;

public class OperadoresRelacionais {

	public static void main(String[] args){
		
		int valor1 = 1;
		int valor2 = 2;
		
		System.out.println("valor1 == valor2 " + (valor1 == valor2)); //operador de comparacao
		System.out.println("valor1 != valor2 " + (valor1 != valor2)); //operador de diferenca
		System.out.println("valor1 > valor2 " + (valor1 > valor2)); //operador de maior que
		System.out.println("valor1 >= valor2 " + (valor1 >= valor2)); //operador de maior ou igual que
		System.out.println("valor1 < valor2 " + (valor1 < valor2)); //operador de menor que
		System.out.println("valor1 <= valor2 " + (valor1 <= valor2)); //operador de menor ou igual que
	}
}
