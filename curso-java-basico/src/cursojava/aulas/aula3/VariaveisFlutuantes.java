package cursojava.aulas.aula3;

public class VariaveisFlutuantes {

	public static void main(String[] args) {
		
		double valorPassagem = 2.90; //8 bytes
		float valorTomate = 3.95f;  //4 bytes (eh necessario um f para indicar que se esta trabalhando com float)

		
		System.out.println("Valor da passagem = " + valorPassagem);
		System.out.println("Valor do tomate = " + valorTomate);
	}

}
